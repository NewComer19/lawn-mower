package AdditionalSystems;

public class AdditionalSystems {

    private boolean isGrassCollector;
    private boolean isGroundAeration;
    private boolean isMulchFunction;

    public AdditionalSystems(boolean isGrassCollector, boolean isGroundAeration, boolean isMulchFunction)
    {
        this.isGrassCollector = isGrassCollector;
        this.isGroundAeration = isGroundAeration;
        this.isMulchFunction = isMulchFunction;
    }

}
